var numOfDrums = document.querySelectorAll(".col").length;

for(var i= 0; i < numOfDrums; i++){
  document.querySelectorAll(".col")[i].addEventListener("click",function(){
    
    var buttonName =this.innerHTML;
    
    checkButtonName(buttonName);
    
  });
}

function checkButtonName(buttonName){
  buttonName = buttonName.toLowerCase();
  switch (buttonName) {
    case "w":
      var tom1 = new Audio("sounds/tom-1.mp3");
      tom1.play();
      break;

    case "a":
      var tom2 = new Audio("sounds/tom-2.mp3");
      tom2.play();
      break;

    case "s":
      var tom3 = new Audio("sounds/tom-3.mp3");
      tom3.play();
      break;

    case "d":
      var tom4 = new Audio("sounds/tom-4.mp3");
      tom4.play();
      break;

    case "j":
      var snare = new Audio("sounds/snare.mp3");
      snare.play();
      break;

    case "k":
      var crash = new Audio("sounds/crash.mp3");
      crash.play();
      break;

    case "l":
      var kick = new Audio("sounds/kick-bass.mp3");
      kick.play();
      break;

    default:
      alert(`Sorry '${buttonName}' doesn't exist`);
      break;
  }
}

document.addEventListener("keypress",function(event){
  var buttonName = event.key;
  buttonName = buttonName.toLowerCase();
  var button = document.querySelector(`.${buttonName}`);
  checkButtonName(buttonName);
  button.classList.add("active");
  setTimeout(function () {
    button.classList.remove("active");
  }, 100);
  function remove_active(button){
    button.classList.remove("active");
  }
  // button.classList.remove("active");
});

